package Array;

public class String_Array2 {
	public static void main(String[] args) {
		//String Homogeneous array
		String aa[]=new String[3];
		aa[0]="Shikha";
		aa[1]="Kalidas";
		aa[2]="Zombade";
		//To extract the data from array we for each loop it,s only use when array is homogeneous 
		for(String AA: aa) {
			System.out.println(AA);			
		}

	}

}

